public class Cozunurluk {     //Monitor classi bu classin sahibi.
    private int en ;
    private int boy;

    public Cozunurluk(int en, int boy) {   //constructor
        this.en = en;
        this.boy = boy;
    }

    public int getEn() {
        return en;
    }

    public void setEn(int en) {
        this.en = en;
    }

    public int getBoy() {
        return boy;
    }

    public void setBoy(int boy) {
        this.boy = boy;
    }
          
    
}
